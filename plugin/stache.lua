vim.api.nvim_create_user_command('StacheOpenTask', function()
    require 'stache'.open_item()
end, {})
vim.api.nvim_create_user_command('StacheRun', function(_)
    require 'stache'.buf_exec_curr_block(0)
end, {})
vim.api.nvim_create_user_command('StacheRunAll', function(_)
    require 'stache'.buf_exec_all_blocks(0)
end, {})
vim.api.nvim_create_user_command('StacheCacheRefresh', function(_)
    require 'stache'.refresh_cache()
end, {})
